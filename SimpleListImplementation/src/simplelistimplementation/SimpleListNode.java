
package simplelistimplementation;

public class SimpleListNode {
    private int value;
    private SimpleListNode next;

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public SimpleListNode getNext() {
        return next;
    }

    public void setNext(SimpleListNode next) {
        this.next = next;
    }
}
